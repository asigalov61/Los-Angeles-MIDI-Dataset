def batched_scores(matbatch, matrix):

  sco= []
  for D in matbatch:

    dist = pairwise_distances(matrix, decompress_matrix(D[1]), metric=matching_type)[0][0]

    if skip_exact_matches:
      if dist == 0:
        dist = 999999

    if dist <= minimum_match_ratio_to_search_for:
      dist = 999999

    sco.append(dist)

  return sco

